function fdot = lorentz3D(t, y)

    beta = 8/3;
    sigma = 10;
    rho = 10;
    
    fdot(1) = sigma * (y(2) - y(1));
    fdot(2) = y(1) * (rho - y(3)) - y(2);
    fdot(3) = y(1) * y(2) - beta * y(3);

    fdot = fdot';

end