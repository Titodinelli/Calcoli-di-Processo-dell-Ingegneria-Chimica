% ----------------------------------------------------------------------- %
%                                                                         %
%                Numerical Integration by means of                        %
%                trapezoidal rule                                         %
%                                                                         %
% ----------------------------------------------------------------------- %

function integral = trapezoidal(f,a,b,ninterval)

    h = (b-a)/ninterval;

    integral = (f(a)+f(b))/2;

    for i = 1:ninterval-1
        integral = integral+f(a+i*h);
    end

    integral = integral * h;

end


