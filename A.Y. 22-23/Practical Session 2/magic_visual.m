clear, close all, clc

% Visually examine the patterns in magic square matrices with orders 
% between 9 and 24 using imagesc. The patterns show that magic uses 
% three different algorithms, depending on whether the value of mod(n,4) 
% is 0, 2, or odd.

for n = 1:16
    subplot(4,4,n)
    ord = n + 8;
    m = magic(ord);
    imagesc(m)
    title(num2str(ord))
    axis equal
    axis off
end